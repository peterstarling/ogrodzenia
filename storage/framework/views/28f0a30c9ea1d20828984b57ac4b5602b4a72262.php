                    <a id="normy">
                        Balustrady według Polskich Norm Budowlanych</a>
                </li>
                <div >
                    Prawo budowlane stawia określone wymagania co do konieczności stosowania balustrad i poręczy. Aktem prawnym obecnie obowiązującym w tym zakresie jest Rozporządzenie Ministra Infrastruktury z dnia 12 kwietnia 2002 r. w sprawie warunków technicznych jakim powinny odpowiadać budynki i ich usytuowanie.<br><br>
                    Zgodnie z art. 296 schody zewnętrzne i wewnętrzne, służące do pokonania wysokości przekraczającej 0,5 m, powinny być zaopatrzone w balustrady lub inne zabezpieczenia od strony przestrzeni otwartej. Jeżeli chodzi o budynki mieszkalne jednorodzinne, zagrodowe i rekreacji indywidualnej warunek wcześniej wspomniany uważa się za spełniony również wówczas, gdy schody i pochylnie o wysokości do 1 m, nie mające balustrad, są obustronnie szersze w stosunku do drzwi lub innego przejścia, do którego prowadzą, co najmniej po 0,5 m.<br><br>
                    Schody zewnętrzne i wewnętrzne, w budynkach użyteczności publicznej powinny mieć balustrady lub poręcze przyścienne, umożliwiające lewo- i prawostronne ich użytkowanie. Przy szerokości biegu schodów większej niż 4 m istnieje wymóg zastosowania dodatkowej balustrady pośredniej.<br><br>
