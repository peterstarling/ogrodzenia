                    <a id="cynkowanie">
                        Co to jest cynkowanie ogniowe?</a>
                </li>
                <div >
                    Cynkowanie ogniowe jest to nałożenie warstwy poprzez zanurzenie w płynnym cynku (450'C) elmentu metalowego. Warstwa cynku jest szczelną powłoką zapobiegającą utlenianiu się stali czyli jej korozji. Materiał przed cynkowaniem jest poddany obróbce chemicznej. Takie zabezpieczenie stali chroni ją przez okres kilkudziesięciu lat (książkowo nawet 40). Okres ten jest uzależniony od środowiska, w jakim się znajduje. <br>
                    Cynkowanie zanurzeniowe profili zamkniętych wymaga wykonania otworów tzw. technologicznych umożliwiające przepłynięciu cynku przez wnętrze profili. W efekcie skutkuje to tzm, że materiał jest ocynkowany również wewnątrz.Jjest najlepszą przemysłową metodą zabezpiecznia antykorozyjnego.
                </div>

                <li>

                    <a id="cynkowanie_wady">
                        Czy cynkowanie ognniowe posiada jakieś wady?</a>
                </li>
                <div  >
                    Cechą cynku jest to, iż utlenia się i rokrocznie jego grubość maleje. Podczas zanurzenia w piecu (wannie) cynkowniczym następuje rozprężanie
                    materiału i w wielu przypadkach powstają stałe odkształcenia materiału - deformacje. Widoczne jest to szczególnie na elementach długich, bądź blachach i drutach. Jednak najbardziej istotną zmianą jaką zachodzi podczas procesu cynkowania ogniowego istotną dla klienta jest utrata jakości powierzchni. Podczas wyciągania elementu z pieca cynkowniczego, gdzie gęstość roztopionego cynku jest 8-krotnie gęstsza od cieczy bardzo szybko zastyga tworząc zacieki i zlewy - nadając materiałowi strukturalną poweirzchnię. Ponadto do poweirzni materiału przyklejają się tzw. popioły, zamieczyszczając ją.
                </div>

