                    <a id="tig">
                        Spawanie metodą TIG – co to znaczy?</a>
                </li>
                <div  >
                    Spawanie metodą TIG (Tungsten Inert Gas) polega na wytwarzaniu łuku elektrycznego za pomocą nietopliwej elektrody wolframowej w osłonie gazu obojętnego.<br><br>
                    Łuk spawalniczy występujący między nietopliwą elektrodą a materiałem spawanym topi powierzchnię materiału. W spawaniu TIG nie jest konieczne stosowanie materiału dodatkowego. Elementy spawane można łączyć przez przetopienie rowka spawalniczego. Jeżeli jednak stosowany jest materiał dodatkowy, jest on wprowadzany do jeziorka w sposób ręczny, a nie za pomocą uchwytu spawalniczego tak jak w metodzie MIG/MAG. Dlatego w spawaniu TIG, uchwyt spawalniczy posiada zupełnie inną konstrukcję niż uchwyt stosowany w metodzie MIG/MAG.<br><br>
                    Metoda TIG umożliwia uzyskanie spoiny niezwykle czystej i wysokiej jakości. W procesie nie powstaje żużel, co eliminuje ryzyko zanieczyszczenia spoiny jego wtrąceniami a gotowa spoina praktycznie nie wymaga żadnego czyszczenia. Metoda TIG jest najczęściej stosowana do spawaniastali nierdzewnych i innych stali wysokostopowych oraz takich materiałów jak  aluminium,miedź, tytan, nikiel i ich stopów.
                    Spawanie TIG jest wykorzystywane między innymi do spawania rur i rurociągów oraz cienkich blach. Jest stosowane w różnych gałęziach przemysłu, m. in. spożywczym, chemicznym, samochodowym, lotnictwie.<br><br>
                    Główne zalety metody TIG zastosowanej do stali nierdzewnych można podsumować w sposób
                    następujący:<br>
                    - skoncentrowane źródło ciepła powodujące powstanie wąskiej strefy stopienia,<br>
                    - bardzo stabilny łuk i spokojne, niewielkie jeziorko spawalnicze; nie ma rozprysków i ponieważ nie ma potrzeby stosowania topnika w tej metodzie, wyeliminowane są pozostałości utleniania, co upraszcza znacznie problem końcowego czyszczenia,<br>
                    - doskonała jakość metalurgiczna z dokładną kontrolą wtopienia i kształtu spoiny we wszystkich pozycjach spawania,<br>
                    - dobre spoiny wolne od porów,<br>
                    - bardzo małe zużycie elektrod
                </div>

