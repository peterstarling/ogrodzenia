                <div >
                    Dobrze zaprojektowane i wykonane schody muszą spełniać szereg warunków gwarantujących bezpieczeństwo oraz wygodę użytkowania.Wymiary stopni. Szerokość i wysokość stopni schodów powinny być dostosowane do przeciętnej długości kroku ludzkiego, która wynosi 60-65 cm.
                    <img src="/media/schody.jpg" class="img-responsive center-block" alt="Responsive image">
                    Ustalono, że zmiana wysokości stopnia musi pociągać za sobą odpowiednią zmianę jego szerokości i że wzajemny związek między tymi wielkościami powinien wynosić : 2h + s = 60-65 cm w zależności tej : h - wysokość stopnia [ cm ], s - szerokość stopnia [ cm ].<br><br>
                    Wysokość stopni h, zgodnie z przepisami budowlanymi, nie powinna przekraczać:<br>
                    20 cm w schodach prowadzących na strych lub do piwnicy oraz przeznaczonych do okresowej obsługi urządzeń przemysłowych,<br>
                    19 cm w domkach jednorodzinnych i mieszkaniach dwukondygancyjnych,<br>
                    16 cm w budynkach użyteczności publicznej,<br>
                    17,5 cm w pozostałych wypadkach.
                    <img src="/media/stopnie.jpg" class="img-responsive center-block" alt="Responsive image">
                    Liczba stopni. W jednym biegu nie powinno być więcej niż 18 stopni, najczęściej ze względu na zmęczenie użytkowników, biegi mają po 9-12 stopni. W schodach wewnętrznych nie należy stosować biegów mających mniej niż 3 stopnie, gdyż łatwo się wtedy potknąć.<br><br>
