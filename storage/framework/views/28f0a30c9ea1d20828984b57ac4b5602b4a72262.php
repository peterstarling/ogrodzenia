                    <a id="technika">
                        Jaką techniką są malowane ogrodzenia?</a>
                </li>
                <div >
                    Elementy ogrodzeń malowane są farbami proszkowymi. Naładowane elektrostatycznie cząsteczki farby przywierają równomiernie do malowanego przedmiotu. Po naniesieniu farba jest utwardzona w piecu w temp ok. 200 °C . W malowaniu proszkowym nie występuje elekt zacieków.<br><br>
                    Malowanie proszkowe bram przesuwnych i skrzydłowych, balustrad balkonowych, ogrodzeń stalowych lub innych elementów wykonanych ze stali przez polega na nakładaniu farby proszkowej na powłokę metalu. W malowaniu proszkowym stosuje się technikę natrysku elektrostatycznego lub elektrokinetycznego. Wcześniej powłoka przykładowo bramy przesuwnej jest odpowiednio przygotowywana przez operacje odtłuszczania, trawienia i fosforowania.<br><br>
                    Po nałożeniu farby na powierzchnię bram, balustrad czy przęseł ogrodzeniowych następuje etap polimeryzacji, czyli wygrzewania w wysokich temperaturach (ok. 200°C). W rezultacie wygrzewania farba ulega stopieniu na powierzchniach metalowych. Powłoka lakiernicza uzyskana w ten sposób, poza walorami estetycznymi chroni elementy stalowe wyprodukowane przez naszą firmę przed korozją, chemikaliami, wysoką temperaturą i uszkodzeniami mechanicznymi, a także działaniem warunków atmosferycznych.<br><br>
                    Przed malowaniem proszkowym bram, balustrad czy ogrodzeń stalowych montowanych na zewnątrz budynków zaleca się zastosowanie podładu przez ocynkowanie ogniowe lub galwaniczne, co dodatkowo zwiększa odporność na korozję.
